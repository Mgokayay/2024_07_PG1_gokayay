package odevler.bolum01;

public class Q08 {
    public static void main(String[] args) {
        final double PI = 3.14159;

        double radius = 5.5;

        double perimeter = 2 * PI * radius;
        System.out.println(perimeter);

        double area = PI * radius * radius;
        System.out.println(area);

    }
}
